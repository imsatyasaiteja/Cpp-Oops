#include <iostream>
using namespace std;

int main()
{
    string name = "John";                                 // string is a datatype to store words
    int age = 35;                                         // int is an integer datatype
    cout << "There was once a man called " << name << endl;
    cout << "He was " << age << " years old" << endl;
    cout << "He liked the name " << name << endl;
    cout << "But did not like being " << age << endl;
}