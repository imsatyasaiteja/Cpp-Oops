#include<iostream>
#include<cmath>
using namespace std;

int main()
{
    // pow() function :
    cout << pow(3, 4) << "\n";
    // sqrt() function :
    cout << sqrt(36.4) << "\n";
    // round() function :
    cout << round(4.6) << "\n";
    // ceil() and floor() functions :
    cout << ceil(4.6544) << "\n";
    cout << floor(4.653) << "\n";
    // fmax() and fmin() functions :
    cout << fmax(3, 10) << "\n";
    cout << fmin(-2.5, 5) << "\n";

    return 0;
}